#include<iostream>

using namespace std;

int main(){

    int x;

    cout << "Enter a number: ";
    cin >> x; // Taking input from the user and storing it in the variable x
    cout << "value of x is: " << x << endl; // Outputting the value of x to the console

    int y, z;
    cout << "Enter two numbers: ";
    cin >> y >> z; // Taking input for two variables y and z
    cout << "value of y is : " << y << "and value of z is : " << z << endl; // Outputting the values of y and z

    return 0;
}

